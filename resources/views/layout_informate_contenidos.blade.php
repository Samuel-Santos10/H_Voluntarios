                <h5 id="problemasEdu">Problemas educativos</h5>

                <!--IMAGENES -->

                <p>
                    Los problemas educativos se basan en los siguientes factores:
                </p>

                <p>
                    En las instituciones educativas, se presentan conflictos que afectan el rendimiento académico, lo
                    cual provoca el fracaso escolar por diferentes causas, ya sean emocionales, familiares,
                    psicológicas, falta de recursos económicos lo que implica abandonar sus estudios, personales,
                    maltrato por parte de sus familiares, compañeros u otros, lo que implica, entre otras consecuencias,
                    apatía y falta de interés a los estudios. <br> <br>

                    En el ámbito educativo, existen diversas problemáticas que influyen en el rendimiento escolar del
                    sujeto; una de ellas, lo constituye el acoso escolar o bullying, el cual se presenta a diario en las
                    instituciones educativas a nivel mundial, causando impacto no solo el rendimiento de los afectados,
                    sino también repercutiendo en su vida personal, a raíz del sufrimiento de actos de intimidación,
                    agresión física, verbal, amenazas.
                </p>


                <!--¿Cómo podrías resolver los problemas educativos? -->

                <h5 id="comoResolver">¿Cómo podrías resolver los problemas educativos?</h5>

                <p>
                    Para lograr un aprendizaje significativo, el estudiante debe contar con una serie de factores
                    positivos que contribuyan a su desarrollo integral, tales como:
                </p>

                <ul>
                    <li>Una buena alimentación</li>
                    <li>Un adecuado entorno familiar</li>
                    <li>Buenas relaciones con sus maestros</li>
                    <li>Compañeros de estudio</li>
                    <li>Convivir en un entorno armónico en valores</li>
                    <li>Tener los recursos necesarios </li>
                </ul>


                <p>
                    Sin embargo, los estudiantes no todo el tiempo cuentan con dichos “beneficios”, que le permitan
                    desenvolverse fructíferamente en el ámbito escolar.

                    Es importante, que tanto el núcleo familiar, como la institución educativa y docentes, se encarguen
                    de atacar a tiempo los conflictos que puedan estar afectando el proceso de enseñanza aprendizaje de
                    los niños, niñas y adolescentes, con el fin de garantizar y velar por el derecho a una educación
                    libre de violencia y factores negativos para su crecimiento. <br><br>

                    La educación constituye un tema principal de debate a nivel mundial, debido a que constituye la base
                    fundamental para la construcción del conocimiento y la transmisión de valores éticos, morales y
                    culturales a las nuevas generaciones. En tal sentido, no es secreto para nadie, las problemáticas
                    que existen en las aulas de clase, que pueden afectar el aprendizaje de los estudiantes y por ende,
                    su rendimiento académico.

