                    El programa de formación e inducción está dirigido a todas aquellas personas que quieran contribuir
                    a la educación de una forma altruista y desinteresada. <br>

                    La educación es uno de los factores que más influye en el avance y progreso de personas y
                    sociedades. Además de proveer conocimientos, la educación enriquece la cultura, el espíritu, los
                    valores y todo aquello que nos caracteriza como seres humanos. La educación es necesaria en todos
                    los sentidos. <br> <br>

                    Si te gusta la educación, embárcate en la aventura del voluntariado educativo. Puedes participar en
                    los diferentes programas que ofrecen las organizaciones o empresas para la educación de niños y
                    niñas, jóvenes y adultos mediante programas de apoyo a la lectura, apoyo escolar, alfabetización,
                    educación inclusiva, educación en el tiempo libre, actividades de la comunidad escolar, asociaciones
                    de padres y madres de alumnos entre otras actividades que ayudan al desarrollo educativo.
                </p>


                <!--¿Qué debes saber?-->


                <h5>¿Qué debes saber?</h5>

                <p>
                    Debes tener los conocimientos básicos para poder ayudar en las áreas de voluntariado educativo. <br>

                    En este material de inducción conocerás las áreas educativas donde puedes ayudar a niños, jóvenes o
                    adultos, te presentamos las áreas donde puedes brindar ayuda centros escolares o comunidades, los
                    diferentes problemas que afectan a el desarrollo de la educación, de qué manera puedes ayudar y
                    mejorar la educación de algunas personas que no tienen ese beneficio importante para la sociedad y
                    aprenderás tus responsabilidades como voluntario educativo. <br>

                    La educación es un factor fundamental tanto para las personas como para la sociedad en general, por
                    una parte porque determina conductas y, por otra, porque forma parte de la identidad cultural y del
                    desarrollo constante de la sociedad y, por ende, de un país.
                </p>


                <!--Áreas educativas en las que puedes ayudar-->

                <h5 id="areasEdu">Áreas educativas en las que puedes ayudar</h5>

                <p>
                    Puedes ayudar en comunidades y centros educativos, ayuda a niños/as jóvenes y adultos con:
                </p>

                <h6>1. Apoyo a la lectura</h6>

                <p>
                    Un voluntariado educativo busca que la Lectura Fácil o apoyo a la lectura sea un apoyo para las
                    personas con dificultades de comprensión lectora. Por ejemplo, para personas mayores, personas con
                    discapacidad, o personas que no saben escribir bien.
                </p>

                <h6>2. Alfabetización</h6>

                <p>
                    La alfabetización se logra mediante actividades que se desarrolla para que una persona pueda
                    aprender a escribir y a leer. El voluntariado con personas mayores se encarga de dar apoyo a las
                    personas mayores en su domicilio, en residencias de Tercera edad o en Centros de día. Como
                    voluntario/a en este ámbito puedes realizar labores de acompañamiento, apoyo emocional y
                    asistencial, organización de actividades de ocio, alfabetización digital.
                </p>

                <h6>3. Educación inclusiva</h6>

                <p>
                    La educación inclusiva supone un modelo de educación que pretende atender a las necesidades de todos
                    los niños y niñas, jóvenes y adultos considerando especialmente aquellos casos en los que puede
                    existir un riesgo de exclusión social. No se trata solo de prestar atención a personas con
                    discapacidad sino a todas las personas sin distinguir por la raza, la condición social, la cultura o
                    la religión, entre otros aspectos.
                </p>

                <h6>4. Educación en tiempo libre</h6>

                <p>
                    La educación en el tiempo libre educa en valores a los niños/as y jóvenes en su tiempo libre y es un
                    espacio de formación y participación comunitaria con voluntad de transformación e inclusión social.
                    Puesto que la educación en el tiempo libre es una intervención global en el proceso de formación
                    integral de la personalidad de los niños/as.
                </p>

                <h6>5. Educación en comunidades</h6>

                <p>
                    La educación comunitaria es la participación de padres y organizaciones naturales, docentes,
                    voluntarios y estudiantes en la operación de las unidades educativas centrales, la zona, la región o
                    el nivel plurinacional, en la gestión educativa, en la definición de sus propósitos generales, en la
                    toma de decisiones importantes para la vida escolar, respondiendo de manera apropiada y pertinente a
                    las necesidades de las personas de una comunidad.
                </p>



