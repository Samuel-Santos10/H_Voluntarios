                            <h5>¿Por qué deberías participar en el servicio comunitario?</h5>

                            <p>
                                Existen numerosos beneficios por participar en el servicio comunitario, tanto para ti
                                como para otros. A continuación se presentan algunos de los beneficios más importantes
                                del voluntariado:

                            <ul>
                                <li>Te da una manera de ayudar a otros</li>
                                <li>Ayuda a mejorar tu comunidad</li>
                                <li>Puede ayudar a fortalecer tu curriculum vitae y aplicaciones de la universidad</li>
                                <li>Puede ser una forma de conocer nuevos amigos</li>
                                <li>A menudo resulta en el crecimiento personal</li>
                                <li>Te brinda una manera de obtener experiencia laboral y aprender más sobre ciertos
                                    trabajos</li>
                            </ul>
                            </p>


                            <h5>Motivos para involucrarse</h5>

                            <p>
                            <ul>
                                <li>Se siente bien. La satisfacción y el orgullo de ayudar a otros son motivos
                                    importantes para ofrecerse como voluntario. Cuando uno compromete su tiempo y su
                                    esfuerzo con una organización o una causa a la que adhiere, la sensación de
                                    satisfacción puede ser enorme.</li>
                                <li>Hace más fuerte a su comunidad. Las organizaciones y las agencias que trabajan con
                                    voluntarios ofrecen servicios importantes a bajo costo o sin costo a las personas
                                    que los necesitan. Cuando a una comunidad le va bien, las personas también están
                                    mejor.</li>
                                <li>Puede fortalecer a su familia. El trabajo voluntario es una excelente manera para
                                    que las familias se diviertan y se sientan más unidas. Pero puede resultar difícil
                                    encontrar el tiempo para hacer trabajo voluntario. Por eso, intente reformular parte
                                    del tiempo libre que tienen como familia. Podría seleccionar uno o dos proyectos por
                                    año y convertirlos en una tradición familiar (por ejemplo, hacer y donar canastas de
                                    obsequios a hogares de ancianos para las fiestas).</li>
                            </ul>
                            </p>


                            <h5>¿Aún no sabes dónde ayudar?</h5>

                            <p>
                            Si estás particularmente interesado en trabajar con niños o animales, por ejemplo, puedes encontrar fácilmente actividades de servicios comunitarios más relacionadas con tus intereses. <br><br>

                            Algunas consideraciones a tener en cuenta son:
                            </p>

                            <h6>¿A quién te gustaría ayudar?</h6>

                            <p>
                            ¿Hay un grupo específico de personas o porque te apasionas? Busca proyectos que se relacionen con tu pasión e intereses. También es posible que solo desees realizar actividades de servicio comunitario particulares que te permitan realizar pasatiempos que disfrutes, como hornear o actuar, y eso también está bien.
                            </p>

                            <h6>¿Deseas una actividad de servicio comunitario recurrente o un evento único?</h6>

                            <p>
                            Quizás no tengas suficiente tiempo para dedicarte regularmente al servicio comunitario. En ese caso, puede ser mejor buscar oportunidades que solo ocurren una vez o esporádicamente, como planificar eventos especiales o ayudar a construir una casa.
                            </p>

                            <h6>¿Qué tipo de impacto quieres tener?</h6>

                            <p>
                            Algunas personas prefieren participar en actividades de servicio comunitario que tienen un impacto cuantificable, por ejemplo, actividades en las que conoces la cantidad específica de niños que recibieron tu tutoría, los pesos que recaudaste o las latas de alimentos que recolectaste. Esto contrasta con las actividades que no tienen números tan claros, como crear un jardín o servir como un salvavidas voluntario. Algunas personas prefieren actividades cuantificables porque sienten que lucen más fuertes en las aplicaciones de la universidad o porque simplemente disfrutan al saber su impacto exacto en la comunidad.
                            </p>

                            <h6>¿Qué habilidades te gustaría ganar?</h6>

                            <p>
                            Muchas actividades de servicio comunitario pueden ayudarte a adquirir habilidades. Estas habilidades pueden ser desde la enseñanza hasta la medicina, la construcción y más. Si hay una habilidad en particular que te gustaría aprender para futuras clases, trabajos o simplemente por interés personal, es posible que desees ver si hay una actividad de servicio a la comunidad que te ayude a aprender esa habilidad.
                            </p>



