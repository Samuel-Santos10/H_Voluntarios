                <h5 id="areasNatu">Áreas naturales- Pérdida de hábitats</h5>

                <h6>Rios y lagos</h6>

                <!-- IMAGNES PEQUENAS de rios y lagos-->

                <p>
                    Los ríos son áreas naturales de mayor contaminación existen muchos tipos de contaminación hacia los
                    ríos
                    y
                    lagos pero nos centramos en qué es la contaminación del agua de lagos y ríos y los tipos de agentes
                    contaminantes que la causan, todos relacionados con las acciones del ser humano y con las
                    actividades
                    que
                    desarrolla. La contaminación hídrica en ríos, lagos y lagunas se da cuando la calidad de las aguas
                    queda
                    comprometida por la presencia de agentes de diverso origen que la desequilibran, haciéndolano apta
                    para
                    el consumo y, por ende, dificultando la vida en ella.
                </p>

                <h6>Bosques</h6>

                <!-- IMAGNES PEQUENAS de bosques-->

                <p>
                    Millones de hectáreas de bosque se pierden al año. Si no se controla la explotación irracional de
                    nuestros bosques, las consecuencias ya en sí desastrosas, serán aún más graves. La pérdida de
                    árboles y
                    otra vegetación puede causar el cambio climático, desertificación, erosión del suelo, daños a la
                    calidad
                    de la tierra ya que los suelos forestales son húmedos, inundaciones, aumento de los gases de efecto
                    invernadero en la atmósfera que aumentan el calentamiento global.
                </p>

                <h6>Suelos</h6>

                <!-- IMAGNES PEQUENAS de suelos-->

                <p>
                    Un suelo contaminado es aquel cuyas características han sido alteradas negativamente por la
                    presencia de
                    componentes químicos de carácter peligroso procedentes de la actividad humana, en concentración tal
                    que
                    comporte un riesgo inaceptable para la salud humana o el medio ambiente. La contaminación del suelo
                    es
                    un problema global que tiene soluciones muy costosas y complicadas. Es por eso que es importante
                    prevenir la contaminación antes de que se produzca, aunque no siempre es posible.
                </p>


                <h6>Flor y fauna</h6>

                <!-- IMAGNES PEQUENAS de Flor y fauna-->

                <p>
                    El aumento en la presencia sustancias químicas en el ambiente como resultado de las actividades
                    humanas
                    tiene graves consecuencias para muchas especies. Las actividades industriales, agrícolas, ganaderas
                    y
                    urbanas contribuyen substancialmente a la contaminación de aire, agua y suelos. Por mucho tiempo la
                    contaminación fue un problema de una escala espacial pequeña, sin embargo actualmente la producción
                    de
                    contaminantes afecta a todo el planeta. Algunos contaminantes han debilitado la capa de ozono que
                    protege a los seres vivos de las radiaciones ultravioletas del Sol, mientras que otros han provocado
                    el
                    calentamiento global. La contaminación del agua, del suelo y del aire afecta directamente a muchos
                    organismos aun en lugares remotos.
                </p>


                <h6>Ecosistema</h6>

                <!-- IMAGNES PEQUENAS de ECOSISTEMA-->

                <p>
                    Al transformar selvas, bosques, matorrales, pastizales, manglares, lagunas, y arrecifes en campos
                    agrícolas, ganaderos, granjas camaroneras, presas, carreteras y zonas urbanas destruimos el hábitat
                    de
                    miles de especies. Muchas veces la transformación no es completa pero existe deterioro de la
                    composición, estructura o función de los ecosistemas que impacta a las especies y a los bienes y
                    servicios que obtenemos de la naturaleza.
                </p>

                <!-- Problemas ambientales-->

                <h5 id="problemas">Problemas ambientales</h5>

                <!-- IMAGNES PEQUENAS de Problemas ambientales-->

                <h6>1. Contaminación</h6>

                <p>
                    Ni la tierra, ni el aire, ni el resto de recursos tienen la misma calidad que hace unos años. Los
                    gases
                    de efecto invernadero han deteriorado dichos recursos, la mano del hombre a sido un factor fuerte en
                    la
                    contaminacion.
                </p>

                <h6>2. Deforestación</h6>

                <p>
                    Cada año desaparecen miles de kilómetros de bosques en el mundo. Se calcula
                    que, desde 1990, se han destruido más de la mitad de los que observamos ahora. Además, los
                    árboles mueren a un ritmo acelerado debido a la contaminación del aire, la sequía de fuentes
                    hídricas aledañas y los cambios bruscos de temperatura.
                </p>

                <h6>3. Sobreexplotación de los recursos naturales</h6>

                <p>
                    Los recursos naturales pronto serán insuficientes para la supervivencia de las
                    personas que habitan la Tierra. La sobreexplotación de los recursos, el consumo irresponsable y
                    el aumento de la población mundial, que se ha triplicado en los últimos 60 años y continúa
                    creciendo a un ritmo acelerado, son las principales causas de este agotamiento. El agua y la
                    energía disponibles no darán abasto para suplir tales necesidades. Además, la explotación de
                    dichos recursos sigue en manos de empresas de países desarrollados que generan escasos
                    beneficios en los sitios donde las extraen, lo que aumenta la desigualdad y obliga a muchas
                    personas a abandonar su sitio de residencia en busca de zonas más aptas para la supervivencia.
                </p>


                <h6>4. Uso de fertilizantes químicos y métodos artificiales de cultivo</h6>

                <p>
                    Las sequías, los huracanes y otros fenómenos ambientales impiden la producción
                    y el cultivo de alimentos en condiciones normales. Con el fin de enmendar estos efectos y
                    multiplicar la producción, se han sustituido los métodos de producción natural por otros de tipo
                    artificial y en los que predomina el uso de sustancias químicas. Estos métodos de cultivo hacen
                    que los alimentos sean de peor calidad y afectan negativamente a nuestra salud y la del
                    medioambiente, pues contaminan los alimentos, la tierra y el aire.
                </p>


                <!-- ¿Sabes qué problemas ambientales son más frecuentes en la ciudad?-->


                <h5 id="sabesQue">¿Sabes qué problemas ambientales son más frecuentes en la ciudad?</h5>

                <p>
                    Los problemas ambientales suelen afectar más a las áreas urbanas: las ciudades, sean más grandes o
                    más
                    pequeñas, concentran un mayor número de población y espacios industriales, producen más residuos,
                    tienen
                    un
                    índice de vehículos más alto, los espacios naturales son más pequeños, el aire está contaminado,
                    entre
                    otras
                    cuestiones. ¡Y todo esto, claro está, incide en nuestra salud y bienestar!.
                </p>


                <!-- IMAGNES PEQUENAS de problemas ambientales son más frecuentes-->

                <h6>1. Falta de zonas verdes</h6>

                <p>
                    Las ciudades, en su mayoría, cuentan con pocos espacios verdes, especialmente
                    aquellas con planificaciones urbanísticas más antiguas y, por tanto, menos preocupadas por la
                    calidad del ambiente urbano y del planeta. Numerosos estudios, los parques son esenciales para
                    purificar el aire, aportan mayor calidad de vida y son espacios de interacción y relajación para los
                    ciudadanos. Sin árboles y plantas no hay fotosíntesis, de manera que el aire no se renueva y empeora
                    su calidad: esto influye directamente en la salud de las personas y provoca, por ejemplo,
                    enfermedades respiratorias.
                </p>

                <h6>2. Contaminación acústica</h6>

                <p>
                    Las fábricas, el exceso de vehículos, los centros comerciales, los locales de ocio
                    nocturno… La exposición a un nivel de ruido demasiado alto y de forma continuada repercute en
                    nuestros oídos, pero también en otros aspectos de nuestra vida: causa problemas coronarios,
                    agresividad, problemas hormonales, entre otros.
                </p>

                <h6>3. Contaminación visual</h6>

                <p>
                    En las áreas urbanas se concentran un gran número de datos visuales: anuncios,
                    carteles, escaparates… Y todos ellos hacen que nuestro cerebro se mantenga constantemente activo, lo
                    cual puede desencadenar estrés o cefaleas. Ante ello, muchas ciudades en el mundo y en nuestro país,
                    como es el caso de Ourense, trabajan insistentemente en diseñar normativas que garanticen un
                    equilibrio de la comunicación visual con los espacios públicos en donde se exhiben.
                </p>

                <h6>4. Acumulación de basura y de aguas residuales</h6>

                <p>
                    La acumulación de los residuos es un problema a tener muy en cuenta en las
                    ciudades: una vez se deposita desde nuestros hogares, un fallo en su gestión puede acabar
                    desencadenando problemas con insectos o roedores. Pero, yendo más allá, una mala gestión en las
                    plantas de tratado de residuos puede acarrear terribles consecuencias para el entorno, tal como
                    sucede con las aguas residuales, que muchas veces son vertidas en los ríos o en el mar. No obstante,
                    este es uno de los problemas ambientales que más se ha frenado en las últimas décadas.
                </p>


                <h6>5. Congestión de tráfico</h6>

                <p>
                    El exceso de vehículos en las ciudades provoca problemas de contaminación del aire
                    y empeora la calidad de vida de los ciudadanos: es causa de estrés y de diferentes problemas de
                    salud, por lo que es uno de los problemas ambientales que más hay que tener en cuenta de cara al
                    futuro. Las ciudades con más habitantes son, por defecto, las que más sufren estos problemas. Nueva
                    York, Ciudad de México, Pekín o Río der Janeiro son algunos ejemplos claros de ello. <br>


                    Las ciudades y áreas urbanas se ven expuestas a muchos otros problemas ambientales. Sin embargo,
                    como casi siempre, en nuestra mano está tomar medidas para revertir, en la medida de lo posible, su
                    impacto sobre nuestro entorno más inmediato. Hoy puede es buen día para empezar.
                </p>


                <!-- ¿Cuáles acciones podrían tomarse para resolver los problemas ambientales?-->

                <h5 id="cualesAcciones">¿Cuáles acciones podrían tomarse para resolver los problemas ambientales?</h5>

                <!-- IMAGNES PEQUENAS de ¿Cuáles acciones podrían tomarse para resolver los problemas ambientales?-->

                <p>
                    No obstante la gravedad de estos hechos, las ciudades y áreas urbanas se ven expuestas a otros
                    problemas
                    ambientales. Como casi siempre, en nuestra mano está tomar medidas para revertir, en la medida de lo
                    posible, su impacto sobre nuestro entorno más inmediato. Hoy puede es buen día para empezar a tomar
                    partido; por eso te damos algunas ideas. Toma nota de ellas:

                <ul>

                    <li>Apuesta por desplazarte en transporte público o, siempre que te sea posible, en bicicleta o a
