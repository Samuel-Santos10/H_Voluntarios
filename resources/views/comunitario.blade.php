        <p>
        ¿Cuál es la mejor forma de cambiar la sociedad, enfrentarse a la desigualdad o ayudar a quienes más lo necesitan? Por supuesto, desde las instituciones y organismos públicos se puede hacer mucho, pero nosotros y nosotras, como conjunto de la ciudadanía, también podemos aportar nuestro granito de arena para mejorar diferentes ámbitos y hacer el mundo un poquito más justo. ¿Sabes cómo? ¡Hay varias maneras! Por ejemplo, el trabajo comunitario. <br>


        ¿Qué es el trabajo comunitario?
        Es una manera de participación social y mejora de la que tú también puedes formar parte, y que es puesta en marcha por diferentes instituciones que buscan cambiar las cosas poco a poco y desde la ciudadanía, con la ayuda de personas voluntarias preocupadas por ayudar a quien lo necesite. <br>

        En este material de inducción conocerás las áreas en las que puedes brindar ayuda a la comunidad, conocerás los problemas que existen en las comunidades, la solución para algunos de estos problemas, como puedes poner esas soluciones en práctica y la importancia de ser un voluntario comunitario.

