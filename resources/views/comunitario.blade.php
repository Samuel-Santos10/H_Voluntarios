        <div class="row py-4">
        <h6> Motivos para involucrarse</h6> <br>

        <img src="img/motivos.jpg" height="400px" alt="">

        <ul>
        <li>Se siente bien. La satisfacción y el orgullo de ayudar a otros son motivos importantes para ofrecerse como voluntario. Cuando uno compromete su tiempo y su esfuerzo con una organización o una causa a la que adhiere, la sensación de satisfacción puede ser enorme.</li>
        <li>Hace más fuerte a su comunidad. Las organizaciones y las agencias que trabajan con voluntarios ofrecen servicios importantes a bajo costo o sin costo a las personas que los necesitan. Cuando a una comunidad le va bien, las personas también están mejor.</li>
        <li>Puede fortalecer a su familia. El trabajo voluntario es una excelente manera para que las familias se diviertan y se sientan más unidas. Pero puede resultar difícil encontrar el tiempo para hacer trabajo voluntario. Por eso, intente reformular parte del tiempo libre que tienen como familia. Podría seleccionar uno o dos proyectos por año y convertirlos en una tradición familiar (por ejemplo, hacer y donar canastas de obsequios a hogares de ancianos para las fiestas).</li>
        </ul>

