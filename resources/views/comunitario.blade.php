        <div class="row py-4">
        <h6>¿Aún no sabes dónde ayudar?</h6> <br>

        <img src="img/co11.jpg" height="500px" alt="">

        <p>
        Si estás particularmente interesado en trabajar con niños o animales, por ejemplo, puedes encontrar fácilmente actividades de servicios comunitarios más relacionadas con tus intereses. <br>

        Algunas consideraciones a tener en cuenta son: <br> <br>

        ¿A quién te gustaría ayudar? <br> <br>

        ¿Hay un grupo específico de personas o porque te apasionas? Busca proyectos que se relacionen con tu pasión e intereses. También es posible que solo desees realizar actividades de servicio comunitario particulares que te permitan realizar pasatiempos que disfrutes, como hornear o actuar, y eso también está bien. <br> <br>

        ¿Deseas una actividad de servicio comunitario recurrente o un evento único? <br> <br>

        Quizás no tengas suficiente tiempo para dedicarte regularmente al servicio comunitario. En ese caso, puede ser mejor buscar oportunidades que solo ocurren una vez o esporádicamente, como planificar eventos especiales o ayudar a construir una casa. <br> <br>

        ¿Qué tipo de impacto quieres tener? <br> <br>

        Algunas personas prefieren participar en actividades de servicio comunitario que tienen un impacto cuantificable, por ejemplo, actividades en las que conoces la cantidad específica de niños que recibieron tu tutoría, los pesos que recaudaste o las latas de alimentos que recolectaste. Esto contrasta con las actividades que no tienen números tan claros, como crear un jardín o servir como un salvavidas voluntario. Algunas personas prefieren actividades cuantificables porque sienten que lucen más fuertes en las aplicaciones de la universidad o porque simplemente disfrutan al saber su impacto exacto en la comunidad. <br> <br>

        ¿Qué habilidades te gustaría ganar? <br> <br>

        Muchas actividades de servicio comunitario pueden ayudarte a adquirir habilidades. Estas habilidades pueden ser desde la enseñanza hasta la medicina, la construcción y más. Si hay una habilidad en particular que te gustaría aprender para futuras clases, trabajos o simplemente por interés personal, es posible que desees ver si hay una actividad de servicio a la comunidad que te ayude a aprender esa habilidad.

        </p>
