        El voluntariado comunitario te permitirá participar en movimientos cívicos, vecinales, colectivos y de participación ciudadana para el desarrollo y la cohesión de la comunidad. Participando en proyectos de voluntariado comunitario podrás generar efectos positivos sobre tu propia comunidad mediante actividades educativas, de ocio y tiempo libre, recreativas, deportivas, culturales, de consumo responsable, etc. <br>

        El voluntariado de ocio y tiempo libre colabora con las entidades que desarrollan actividades socio-educativas, culturales, deportivas e incluso actividades relacionadas con el medio ambiente con el fin de potenciar la educación y el desarrollo comunitario. <br>

