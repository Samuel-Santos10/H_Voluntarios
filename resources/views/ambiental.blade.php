                    <p class="card-text">Las sequías, los huracanes y otros fenómenos ambientales impiden la producción
                        y el cultivo de alimentos en condiciones normales. Con el fin de enmendar estos efectos y
                        multiplicar la producción, se han sustituido los métodos de producción natural por otros de tipo
                        artificial y en los que predomina el uso de sustancias químicas. Estos métodos de cultivo hacen
                        que los alimentos sean de peor calidad y afectan negativamente a nuestra salud y la del
                        medioambiente, pues contaminan los alimentos, la tierra y el aire.</p>
