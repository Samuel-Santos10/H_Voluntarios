        <div class="col text-center">
            <p>
                No obstante la gravedad de estos hechos, las ciudades y áreas urbanas se ven expuestas a otros problemas
                ambientales. Como casi siempre, en nuestra mano está tomar medidas para revertir, en la medida de lo
                posible, su impacto sobre nuestro entorno más inmediato. Hoy puede es buen día para empezar a tomar
                partido; por eso te damos algunas ideas. Toma nota de ellas: <br>
            </p>
            <ul>

                <li>Apuesta por desplazarte en transporte público o, siempre que te sea posible, en bicicleta o a pie
                </li>
                <li>Exige al ayuntamiento de tu ciudad que tome medidas para regular los vertidos y emisiones de las
                    industrias cercanas.</li>
                <li>Ahorra energía de cualquier tipo. Aunque la energía eléctrica no implica directamente un proceso de
                    combustión, su producción sí que la precisa en el caso de las centrales térmicas. Fomenta el ahorro
                    de energía mediante los sistemas de regulación de la temperatura de los edificios, el aislamiento
                    térmico y el rendimiento de las calefacciones.</li>
                <li>No practiques conductas ruidosas en casa: gritos, taconeos, portazos, utilización de
                    electrodomésticos en períodos de descanso.</li>
                <li>Reclama una buena gestión de las zonas verdes, con especies autóctonas que mantengan el ecosistema.
                </li>
                <li>Utiliza materiales biodegradables siempre que tengas oportunidad de ello</li>
                <li>Practica un consumo responsable y recicla lo que puedas.</li>
            </ul>

