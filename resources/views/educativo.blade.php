        En las instituciones educativas, se presentan conflictos que afectan el rendimiento académico, lo cual provoca el fracaso escolar por diferentes causas, ya sean emocionales, familiares, psicológicas, falta de recursos económicos lo que implica abandonar sus estudios, personales, maltrato por parte de sus familiares, compañeros u otros, lo que implica, entre otras consecuencias, apatía y falta de interés a los estudios. <br>

