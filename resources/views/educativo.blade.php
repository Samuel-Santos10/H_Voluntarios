        <div class="row py-4">
        <h6>¿Cómo podrías resolver los problemas educativos?</h6> <br>

        <img src="img/ed9.jpg" alt="">

        <p>
        Para lograr un aprendizaje significativo, el estudiante debe contar con una serie de factores positivos que contribuyan a su desarrollo integral, tales como: <br>
        </p>

        <ul>
        <li>•	una buena alimentación</li>
        <li>•	un adecuado entorno familiar</li>
        <li>•	buenas relaciones con sus maestros</li>
        <li>•	compañeros de estudio</li>
        <li>•	convivir en un entorno armónico en valores/li>
        <li>•	tener los recursos necesarios </li>
        </ul>

        <p>
        Sin embargo, los estudiantes no todo el tiempo cuentan con dichos “beneficios”, que le permitan desenvolverse fructíferamente en el ámbito escolar. <br>
        
        Es importante, que tanto el núcleo familiar, como la institución educativa y docentes, se encarguen de atacar a tiempo los conflictos que puedan estar afectando el proceso de enseñanza aprendizaje de los niños, niñas y adolescentes, con el fin de garantizar y velar por el derecho a una educación libre de violencia y factores negativos para su crecimiento. <br>

        La educación constituye un tema principal de debate a nivel mundial, debido a que constituye la base fundamental para la construcción del conocimiento y la transmisión de valores éticos, morales y culturales a las nuevas generaciones. En tal sentido, no es secreto para nadie, las problemáticas que existen en las aulas de clase, que pueden afectar el aprendizaje de los estudiantes y por ende, su rendimiento académico.

        </p>
