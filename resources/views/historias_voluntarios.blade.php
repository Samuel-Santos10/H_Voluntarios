                                <h3 class="animate__animated animate__bounce">La historia de Manuel de los Ángeles Vargas</h3>
                                <p>
                                En nombre de Manuel de los Ángeles Vargas, de 23 años, recibió el Premio Fundación Esplai a la Ciudadanía Comprometida en el año 2015, en la categoría «Trabajo por la E-inclusión», por su labor voluntaria en el telecentro Red Conecta de Jovesolides, en Paterna-Valencia, en el barrio de La Coma. Por el cariño, entusiasmo, actitud de superación y responsabilidad que el joven pone en cada una de las actividades en las que participa. <br>

                                Por el proceso que ha pasado, siendo primero participante del telecentro y convirtiéndose después en voluntario en una zona castigada con altos índices de analfabetismo, exclusión digital, uso de sustancias psicoactivas y pocas oportunidades sociales y laborales. <br>

                                Hasta hace unos meses, Manuel era uno de los jóvenes beneficiarios de los programas formativos y de atención del telecentro de Jovesolides. Es de origen portugués y cuando emigró con su familia y llegó a La Coma encontró en Jovesolides el apoyo para encontrar su lugar en el barrio. <br>

                                Manuel manifestó por primera vez querer formar parte del equipo de voluntariado de cuando se sorprendió a sí mismo asistiendo diariamente al Telecentro y ayudando a otros vecinos y vecinas de su barrio a usar los ordenadores.

                                El contexto en el que vive Manuel, en el barrio de La Coma, le podría haber llevado a escoger otros caminos de desarrollo personal poco prometedores pero él mismo dio el paso de ser parte del cambio social del barrio, ayudando a personas mayores, mujeres y otros jóvenes a incorporarse de manera activa en la sociedad de la información y la comunicación a través de las nuevas tecnologías.
                                </p>
