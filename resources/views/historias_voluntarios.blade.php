                                <h3>La historia de Sarah Fernández Deutsch</h3>
                                <p>
                                En nombre de Sarah Fernández Deutsch, de 32 años, recibió el Premio Fundación Esplai a la Ciudadanía Comprometida en el año 2015, en la categoría «Acción Socioeducativa», por su trayectoria en el acompañamiento en los procesos de cambio en los territorios. Su impulso comunitario, de la participación y la construcción de ciudadanía comprometida en zonas en desventaja en el sur madrileño y otros lugares del mundo.

                                Por su capacidad de mediación, disposición, compromiso e interlocución en el barrio de San Cristóbal de los Ángeles, en Madrid. Por la capacidad de provocar transformación física, personal, educativa, y de conciencia ciudadana en el territorio, desde la Asociación Basurama en el proyecto Autobarrios.

                                El objetivo de este proyecto ha sido facilitar el desarrollo comunitario en un sentido extenso, aunque focalizando el trabajo más visible en la construcción colectiva de un espacio urbano común para reforzar una comunidad creadora. A través de prácticas creativas, la puesta en valor de los recursos locales y el trabajo en red, se ha generado estrategias para vivir más y mejor el barrio.

                                Autobarrios está liderado por jóvenes del barrio bajo el amparo de las asociaciones locales: Casa San Cristóbal-Fundación Montemadrid y Educación Cultura y Solidaridad y con la ayuda de especialistas reconocidos. El proyecto pretende convertir a los y las jóvenes en cuidadores de su propio espacio vecinal y en facilitadores de la experiencia artística como herramienta educativa transformadora.

                                Sarah es autora, ejecutora y coordinadora de la iniciativa. Es uno de los rostros más visibles de este proyecto coral en el que han participado cientos de vecinos y vecinas.

                                Su responsabilidad principal es diseñar y generar las estrategias adecuadas para el buen desarrollo del proceso y el cumplimiento de los objetivos.
                                </p>
