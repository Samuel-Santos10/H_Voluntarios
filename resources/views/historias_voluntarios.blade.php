                                <h3>La historia de Carmen Quintero Gallego</h3>
                                <p>
                                En nombre de Carmen Quintero Gallego, de 76 años, recibió el Premio Fundación Esplai a la Ciudadanía Comprometida en el año 2015, en la categoría «Acción Socioeducativa», por ser una incombustible impulsora de proyectos educativos y de inserción socio laboral en la Asociación vecinal y la Fundación Rondilla, en Valladolid. Por su trayectoria constante y su mentalidad abierta para introducir y realizar proyectos innovadores que dan respuesta a las necesidades de las personas más vulnerables. Por su más que amplia trayectoria de compromiso personal en la construcción de ciudadanía.

                                Carmen comenzó a colaborar en la asociación en el año 1978 y a día de hoy es voluntaria “a tiempo completo”, es decir, a jornada de 8 horas diarias. Empezó impulsado un proyecto cuyo objetivo era paliar el analfabetismo y bajo nivel cultura de buena parte del vecindario. Fue la impulsora del Círculo de Cultura Popular Rondilla y de su programa de Educación de Personas Adultas. Inició clases de alfabetización, cultura básica, preparación para el Graduado Escolar, etc.

                                Más tarde, viendo que los más jóvenes con fracaso escolar necesitaban una metodología propia y actividades específicas, puso en marcha el programa de integración de menores y jóvenes en riesgo.

                                Con la llegada masiva de personas inmigrantes al barrio, Carmen fue la promotora ofrecer acogida a estas personas y de iniciar un proyecto de integración social y laboral. Hoy día es el alma de este proyecto de tal manera que las personas inmigrantes identifican cualquier actuación con “la señora Carmen”, pues ha dedicado todo su tiempo y su energía a trabajar por y para los colectivos más vulnerables.

                                Actualmente lleva a cabo la asesoría social y jurídica para las personas inmigrantes.

                                Los proyectos sociales, educativos y de integración laboral que realiza actualmente la Fundación Rondilla no serían posibles sin la iniciativa, el trabajo y el impulso de Carmen.

                                Siendo todas las personas del equipo más jóvenes que ella, siempre es ella la que lidera los procesos y arrastra a todo el mundo con su entusiasmo y su dedicación.
                                </p>
