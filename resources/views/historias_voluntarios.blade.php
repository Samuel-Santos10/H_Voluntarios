                                <h3>Valentí Vilaplana, voluntario en Guatemala</h3>
                                <p>
                                En el marco de La Liga Iberoamericana, red a la que pertenecemos,  organizamos estancias de voluntariado internacional en los proyectos educativos que desarrolla la ONG Fe y Alegría en Guatemala. Estas experiencias están dirigidas a personas que trabajan en Fundación Esplai y Fundesplai y que, durante sus vacaciones de verano, colaboran en diferentes proyectos en el país centroamericano.

                                En la mañana del 22 de octubre, hemos celebrado un encuentro de sensibilización sobre la experiencia de voluntariado internacional que he realizado junto a Noelia y Patricia en los proyectos de la organización Fe y Alegría de Guatemala.

                                Durante las vacaciones de verano, en el mes de agosto de 2019, fui acogido en los proyectos educativos de la ONG Fe y Alegría de Guatemala. Junto a Patricia y Noelia estuve colaborando en formaciones dirigidas a los y las docentes que trabajan en las escuelas de Fe y Alegría. El objetivo principal de este voluntariado era fortalecer las capacidades de estos educadores y educadoras, compartiendo experiencias, dinamizando talleres y acompañándoles en su trabajo diario.

                                En concreto he colaborado en el programa «Espacios amigables» en la población de Santa Lucía La Reforma.

                                Gracias a estas semanas de voluntariado allí «he ampliado mi mirada para realizar actividades educativas aquí, en mi trabajo cotidiano. Ahora tengo más recursos y he cambiado, además, mi manera de viajar y querer conocer el mundo»:

                                Gracias a Fe y Alegría de Guatemala que ha posible que pueda enriquecerme con esta experiencia tan transformadora como es colaborar en un voluntariado internacional.
